// Switch case:

var programmingLang = 'JS';

switch (programmingLang) {
    case 'JS':
        console.log('Learning JS')
        break;

        case 'Java':
            console.log('Learning Java')
            break;

        case 'Python':
            console.log('Learning python')
            break;

        default:
            console.log('You are not a programmer')
            break;

}




